    private function _key($key) { //unit ID
        if (!is_array($key)) {
            $key = array('SLOC' => $key);
        }
        return $key;
    }

    public function options_lv1($default = '--Pilih Level 1--', $key = 'all') {
        $option = array();

        $this->db->from('MASTER_LEVEL1');
        if ($key != 'all'){
            $this->db->where('COCODE',$key);
        }   
        $list = $this->db->get(); 

        if (!empty($default)) {
            $option[''] = $default;
        }
            foreach ($list->result() as $row) {
                $option[$row->COCODE] = $row->LEVEL1;
            }
            return $option;    
    }

